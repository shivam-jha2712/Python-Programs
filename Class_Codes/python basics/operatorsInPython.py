x = 12
y = 3

print(x + y)  # adds the values
print(x - y)  # subtracts the values
print(x * y)  # multiplies the values
print(x / y)  # divide and gives out the float division value
print(x // y)  # gives the floor value for the divsion operation
print(x % y)  # gives out the remainder of the value
